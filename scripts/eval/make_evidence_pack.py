# scripts/make_evidence_pack.py
from __future__ import annotations

import argparse
import json
from pathlib import Path

from llm_lab.eval.report import (
    build_evidence_pack_manifest,
    build_kv_memory_economics_table,
    build_ttft_tps_table,
    load_bench_artifacts,
    write_json_artifact,
    write_text_artifact,
)


_REQUIRED_CONTEXTS = {128, 256, 512}
_REQUIRED_BATCHES = {1, 2, 4, 8}


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Build evidence pack artifacts")
    p.add_argument("--bench-dir", type=str, default="experiments/serving_bench")
    p.add_argument("--reports-dir", type=str, default="experiments/serving_reports")
    p.add_argument("--quant-dir", type=str, default="experiments/serving_quant")
    p.add_argument("--out-dir", type=str, default="experiments/serving_reports")
    p.add_argument("--cache-divergence", type=str, default="experiments/serving_bench/cache_divergence_report.json")
    p.add_argument(
        "--cache-receipts",
        type=str,
        default="experiments/serving_bench/cache_equivalence_receipts.json",
        help="Expanded cache-equivalence receipt JSON generated by bench harness.",
    )
    p.add_argument("--package-dir", type=str, default="experiments/p1_pos_enc/runs/rope/package")
    p.add_argument(
        "--validate-grid",
        action="store_true",
        help="Fail if required measured TTFT/TPS context_len x batch_size grid is incomplete.",
    )
    return p.parse_args()


def _load_package_config(package_dir: Path) -> dict:
    config_path = package_dir / "config.json"
    if not config_path.exists():
        raise FileNotFoundError(f"package config not found: {config_path}")
    payload = json.loads(config_path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        raise ValueError(f"invalid package config JSON object: {config_path}")
    return payload


def _bytes_per_elem_from_dtype(dtype: str) -> int:
    d = dtype.lower()
    if "16" in d:
        return 2
    return 4


def _validate_required_grid(rows: list[dict]) -> None:
    mode_points: dict[str, set[tuple[int, int]]] = {"cache": set(), "recompute": set()}
    for row in rows:
        mode = str(row.get("benchmark_mode", "unknown")).lower()
        point = (int(row["context_len"]), int(row["batch_size"]))
        if mode in mode_points:
            mode_points[mode].add(point)

    required = {(c, b) for c in _REQUIRED_CONTEXTS for b in _REQUIRED_BATCHES}
    if mode_points["cache"] and mode_points["recompute"]:
        missing_by_mode: dict[str, list[tuple[int, int]]] = {}
        for mode in ("cache", "recompute"):
            missing = sorted(required - mode_points[mode])
            if missing:
                missing_by_mode[mode] = missing
        if missing_by_mode:
            raise ValueError(f"missing measured TTFT/TPS grid points by mode: {missing_by_mode}")
        return

    present = {(int(r["context_len"]), int(r["batch_size"])) for r in rows}
    missing = sorted(required - present)
    if missing:
        raise ValueError(f"missing measured TTFT/TPS grid points: {missing}")


def _write_cache_equivalence_report(
    *,
    out_path: Path,
    divergence_path: Path,
    receipts_path: Path,
    bench_dir: Path,
) -> None:
    lines: list[str] = []
    lines.append("# Cache Equivalence Report")
    lines.append("")

    receipt_payload: dict[str, object] = {}
    if receipts_path.exists():
        loaded = json.loads(receipts_path.read_text(encoding="utf-8"))
        if isinstance(loaded, dict):
            receipt_payload = loaded

    if divergence_path.exists():
        payload = json.loads(divergence_path.read_text(encoding="utf-8"))
        lines.append("- cache equivalence passed: false")
        lines.append(f"- first divergence artifact: {divergence_path}")
        lines.append(f"- first divergence step: {payload.get('step')}")
        lines.append(f"- max_logit_diff: {payload.get('max_logit_diff')}")
    else:
        all_match = bool(receipt_payload.get("exact_match_generated_tokens_all_cases", True))
        all_top1 = bool(receipt_payload.get("top1_match_all_steps_all_cases", True))
        passed = bool(all_match and all_top1)
        lines.append(f"- cache equivalence passed: {str(passed).lower()}")
        lines.append("- first divergence artifact: none")

    lines.append(f"- tested settings summary source: {bench_dir}")
    lines.append(f"- source receipt file: {receipts_path if receipts_path.exists() else 'none'}")
    lines.append(f"- tested prompt lengths: {receipt_payload.get('tested_prompt_lengths', 'unknown')}")
    lines.append(f"- tested generation lengths: {receipt_payload.get('tested_generation_lengths', 'unknown')}")
    lines.append(f"- dtype: {receipt_payload.get('dtype', 'unknown')}")
    lines.append(f"- quant mode: {receipt_payload.get('quant_mode', 'unknown')}")
    lines.append(f"- greedy setting: {receipt_payload.get('greedy', 'unknown')}")
    lines.append(f"- case count: {receipt_payload.get('case_count', 'unknown')}")
    lines.append(f"- comparable case count: {receipt_payload.get('comparable_case_count', 'unknown')}")
    lines.append(f"- non-comparable case count: {receipt_payload.get('non_comparable_case_count', 'unknown')}")
    lines.append(
        f"- within-mode cached-vs-recompute comparison: {receipt_payload.get('within_mode_cached_vs_recompute', True)}"
    )
    lines.append(f"- tolerance used: {receipt_payload.get('tolerance', '1e-4')}")
    lines.append(f"- provenance: {receipt_payload.get('provenance', 'unknown')}")
    lines.append("")
    out_path.write_text("\n".join(lines), encoding="utf-8")


def _dedupe_table(rows: list[dict]) -> list[dict]:
    chosen: dict[tuple[str, int, int, str, str], dict] = {}
    source_priority = {"_grid.json": 2, "cache.json": 1, "recompute.json": 1}
    for row in rows:
        key = (
            str(row.get("benchmark_mode", "unknown")),
            int(row["context_len"]),
            int(row["batch_size"]),
            str(row["dtype"]),
            str(row["quant_mode"]),
        )
        src = str(row.get("source_file", ""))
        pri = 0
        for suffix, score in source_priority.items():
            if src.endswith(suffix):
                pri = max(pri, score)
        existing = chosen.get(key)
        if existing is None:
            chosen[key] = row
            chosen[key]["_source_priority"] = pri
            continue
        if pri >= int(existing.get("_source_priority", 0)):
            row["_source_priority"] = pri
            chosen[key] = row
    out: list[dict] = []
    for row in chosen.values():
        row = dict(row)
        row.pop("_source_priority", None)
        out.append(row)
    return out


def main() -> None:
    args = parse_args()
    bench_dir = Path(args.bench_dir)
    reports_dir = Path(args.reports_dir)
    out_dir = Path(args.out_dir)
    package_dir = Path(args.package_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    bench_data = load_bench_artifacts(str(bench_dir))
    table = _dedupe_table(build_ttft_tps_table(bench_data))
    if args.validate_grid:
        _validate_required_grid(table)

    ttft_table_path = out_dir / "ttft_tps_table.json"
    write_json_artifact(table, str(ttft_table_path))

    ttft_report_path = out_dir / "ttft_tps_report.md"
    lines: list[str] = []
    lines.append("# TTFT / TPS Report")
    lines.append("")
    lines.append(
        "| benchmark_mode | context_len | batch_size | dtype | quant_mode | prefill_ms | ttft_ms | decode_ms_per_token | tokens_per_sec |"
    )
    lines.append("|---|---:|---:|---|---|---:|---:|---:|---:|")
    for row in sorted(table, key=lambda r: (str(r.get("benchmark_mode", "")), int(r["context_len"]), int(r["batch_size"]))):
        lines.append(
            f"| {row.get('benchmark_mode', 'unknown')} | {row['context_len']} | {row['batch_size']} | {row['dtype']} | {row['quant_mode']} "
            f"| {row['prefill_ms']:.4f} | {row['ttft_ms']:.4f} | {row['decode_ms_per_token']:.4f} "
            f"| {row['tokens_per_sec']:.4f} |"
        )
    write_text_artifact("\n".join(lines) + "\n", str(ttft_report_path))

    package_cfg = _load_package_config(package_dir)
    n_layers = int(package_cfg["n_layers"])
    n_heads = int(package_cfg["n_heads"])
    d_model = int(package_cfg["d_model"])
    if n_heads <= 0 or d_model % n_heads != 0:
        raise ValueError(f"invalid head geometry in package config: n_heads={n_heads}, d_model={d_model}")
    head_dim = d_model // n_heads

    dtype_for_kv = str(table[0]["dtype"]) if table else "float32"
    kv_seq_len = int(table[0]["context_len"]) if table else 256
    kv_batch_size = int(table[0]["batch_size"]) if table else 1
    bytes_per_elem = _bytes_per_elem_from_dtype(dtype_for_kv)
    gqa_heads = max(1, n_heads // 2)
    if gqa_heads == n_heads and n_heads > 1:
        gqa_heads = n_heads - 1

    kv_rows = [
        {
            "arch_variant": "MHA (package-config)",
            "batch_size": kv_batch_size,
            "seq_len": kv_seq_len,
            "n_layers": n_layers,
            "n_kv_heads": n_heads,
            "head_dim": head_dim,
            "bytes_per_elem": bytes_per_elem,
            "dtype": dtype_for_kv,
        },
        {
            "arch_variant": "GQA (hypothetical)",
            "batch_size": kv_batch_size,
            "seq_len": kv_seq_len,
            "n_layers": n_layers,
            "n_kv_heads": gqa_heads,
            "head_dim": head_dim,
            "bytes_per_elem": bytes_per_elem,
            "dtype": dtype_for_kv,
        },
    ]
    kv_table = build_kv_memory_economics_table(kv_rows)
    kv_json_path = out_dir / "kv_cache_memory_economics.json"
    write_json_artifact(kv_table, str(kv_json_path))

    kv_md_path = out_dir / "kv_cache_memory_economics.md"
    kv_lines: list[str] = []
    kv_lines.append("# KV Cache Memory Economics")
    kv_lines.append("")
    kv_lines.append("| arch_variant | batch_size | seq_len | n_layers | n_kv_heads | head_dim | dtype | estimated_kv_cache_bytes | estimated_kv_cache_mb |")
    kv_lines.append("|---|---:|---:|---:|---:|---:|---|---:|---:|")
    for row in kv_table:
        kv_lines.append(
            f"| {row['arch_variant']} | {row['batch_size']} | {row['seq_len']} | {row['n_layers']} "
            f"| {row['n_kv_heads']} | {row['head_dim']} | {row['dtype']} | {row['estimated_kv_cache_bytes']} "
            f"| {row['estimated_kv_cache_mb']:.4f} |"
        )
    write_text_artifact("\n".join(kv_lines) + "\n", str(kv_md_path))

    cache_report_path = out_dir / "cache_equivalence_report.md"
    _write_cache_equivalence_report(
        out_path=cache_report_path,
        divergence_path=Path(args.cache_divergence),
        receipts_path=Path(args.cache_receipts),
        bench_dir=bench_dir,
    )

    manifest_paths = {
        "cache_equivalence_report_md": str(cache_report_path),
        "ttft_tps_table_json": str(ttft_table_path),
        "ttft_tps_report_md": str(ttft_report_path),
        "kv_cache_memory_economics_json": str(kv_json_path),
        "kv_cache_memory_economics_md": str(kv_md_path),
        "eval_manifest_json": str(out_dir / "eval_manifest.json"),
        "reports_dir": str(reports_dir),
        "quant_dir": str(Path(args.quant_dir)),
    }
    manifest = build_evidence_pack_manifest(manifest_paths)
    write_json_artifact(manifest, str(out_dir / "eval_manifest.json"))


if __name__ == "__main__":
    main()
